package main

import "fmt"

func main() {
	// int
	fmt.Println(1)
	fmt.Println(5+6)
	// string
	fmt.Println("HI this is Ankit")
	// bool
	fmt.Println(true)
	fmt.Println(false)
	//float
	fmt.Println(10.5)
	fmt.Println(7.0/3.0)
	//type coercion
	fmt.Println(7.0/3)
	fmt.Println(7/3)
}

// data types -> int, string, bool, float